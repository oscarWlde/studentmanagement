package com.example.demo.util.teacher;

public class return_teacher_course {
    private String courseNum;
    private String courseName;
    private String academyName;
    private String type;
    private String weekdata;
    private String courseMemberNum;

    public String getCourseNum() {
        return courseNum;
    }

    public void setCourseNum(String courseNum) {
        this.courseNum = courseNum;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getAcademyName() {
        return academyName;
    }

    public void setAcademyName(String academyName) {
        this.academyName = academyName;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getWeekdata() {
        return weekdata;
    }

    public void setWeekdata(String weekdata) {
        this.weekdata = weekdata;
    }

    public String getCourseMemberNum() {
        return courseMemberNum;
    }

    public void setCourseMemberNum(String courseMemberNum) {
        this.courseMemberNum = courseMemberNum;
    }
}
