package com.example.demo.bean;


public class Academy {

  private String academyName;
  private long professionNum;
  private long teacherNum;
  private String president;
  private long academyStuNum;
  private String buildingName;


  public String getAcademyName() {
    return academyName;
  }

  public void setAcademyName(String academyName) {
    this.academyName = academyName;
  }


  public long getProfessionNum() {
    return professionNum;
  }

  public void setProfessionNum(long professionNum) {
    this.professionNum = professionNum;
  }


  public long getTeacherNum() {
    return teacherNum;
  }

  public void setTeacherNum(long teacherNum) {
    this.teacherNum = teacherNum;
  }


  public String getPresident() {
    return president;
  }

  public void setPresident(String president) {
    this.president = president;
  }


  public long getAcademyStuNum() {
    return academyStuNum;
  }

  public void setAcademyStuNum(long academyStuNum) {
    this.academyStuNum = academyStuNum;
  }


  public String getBuildingName() {
    return buildingName;
  }

  public void setBuildingName(String buildingName) {
    this.buildingName = buildingName;
  }

}
